// ── Framebuffer state ─────────────────────────────────────────────────────────

struct Fb {
    base:   *mut u32, // start of the pixel buffer
    width:  u32,
    height: u32,
    pitch:  u32,      // bytes per scanline
    cols:   u32,      // text columns (width  / FONT_W)
    rows:   u32,      // text rows    (height / FONT_H)
    col:    u32,      // current cursor column
    row:    u32,      // current cursor row
}

// Safety: we are single-core with no preemption at this stage.
static mut FB: Option<Fb> = None;

// ── Public initialisation ─────────────────────────────────────────────────────

/// Ask the VideoCore GPU for a 32 bpp framebuffer and set up the text
/// console. Returns `true` on success. If it returns `false`, the
/// `FbWriter` is a silent no-op and the kernel continues via UART only.
pub fn init() -> bool {
    unsafe { init_inner() }
}

unsafe fn init_inner() -> bool {
    // Re-declare a local that aliases MBOX, now treating it as 36 u32s.
    // MBOX is 128 bytes (32×4); we only write up to index 35 (36×4=144),
    // which would be out of bounds. Instead, embed a separate buffer here
    // that the compiler allocates at a 16-byte-aligned stack address.
    // For bare-metal with no stack-size concerns this is fine.
    #[repr(C, align(16))]
    struct Buf([u32; 36]);
    let mut buf = Buf([0u32; 36]);
    let m = &mut buf.0;

    m[0]  = 36 * 4; // 144 bytes total
    m[1]  = mailbox::REQUEST;

    // Set physical (display) width × height
    m[2]  = 0x0004_8003; m[3]  = 8; m[4]  = 0;
    m[5]  = WIDTH;       m[6]  = HEIGHT;

    // Set virtual (framebuffer) width × height
    m[7]  = 0x0004_8004; m[8]  = 8; m[9]  = 0;
    m[10] = WIDTH;       m[11] = HEIGHT;

    // Set virtual offset
    m[12] = 0x0004_8009; m[13] = 8; m[14] = 0;
    m[15] = 0;           m[16] = 0;

    // Set depth
    m[17] = 0x0004_8005; m[18] = 4; m[19] = 0;
    m[20] = BPP;

    // Set pixel order: 0 = BGR → write 0x00RRGGBB for correct colours
    m[21] = 0x0004_8006; m[22] = 4; m[23] = 0;
    m[24] = 0;

    // Allocate framebuffer (GPU writes base address + size into [28],[29])
    m[25] = 0x0004_0001; m[26] = 8; m[27] = 0;
    m[28] = 16;          m[29] = 0;

    // Get pitch (bytes per row; GPU writes value into [33])
    m[30] = 0x0004_0008; m[31] = 4; m[32] = 0;
    m[33] = 0;

    // End tag
    m[34] = 0;
    m[35] = 0; // padding to fill 36 slots

    // Send to GPU
    if !mailbox::call(m.as_mut_ptr()) {
        return false;
    }

    let fb_addr = m[28];
    let pitch   = m[33];

    if fb_addr == 0 || pitch == 0 {
        return false;
    }

    // On Pi 4, the returned address is a direct physical address.
    // On Pi 3, mask off the bus-address bits: addr & 0x3FFF_FFFF
    #[cfg(feature = "bsp_rpi3")]
    let base = (fb_addr & 0x3FFF_FFFF) as *mut u32;
    #[cfg(feature = "bsp_rpi4")]
    let base = fb_addr as *mut u32;

    let actual_w = m[5];
    let actual_h = m[6];

    FB = Some(Fb {
        base,
        width:  actual_w,
        height: actual_h,
        pitch,
        cols: actual_w / FONT_W,
        rows: actual_h / FONT_H,
        col: 0,
        row: 0,
    });

    // Clear screen to background colour
    clear_screen();
    true
}

// ── Drawing primitives ────────────────────────────────────────────────────────

/// Fill the entire screen with the background colour.
pub fn clear_screen() {
    // Safety: called after FB is initialised.
    unsafe {
        if let Some(ref mut fb) = FB {
            let pixels = (fb.pitch / 4) * fb.height; // total u32 words
            for i in 0..pixels {
                fb.base.add(i as usize).write_volatile(BG);
            }
            fb.col = 0;
            fb.row = 0;
        }
    }
}

/// Draw one 8×8 character at pixel position (px, py).
fn draw_char(c: u8, px: u32, py: u32) {
    // Safety: called from write_byte which checks FB is Some.
    unsafe {
        let fb = match FB {
            Some(ref mut f) => f,
            None => return,
        };

        let idx = if c >= 0x20 && c <= 0x7F {
            (c - 0x20) as usize
        } else {
            0 // render unknown chars as space
        };
        let glyph = &FONT[idx];

        let pitch_words = fb.pitch / 4;
        for row in 0..FONT_H {
            let byte = glyph[row as usize];
            for col in 0..FONT_W {
                let pixel = if byte & (0x80 >> col) != 0 { FG } else { BG };
                let x = px + col;
                let y = py + row;
                if x < fb.width && y < fb.height {
                    fb.base
                        .add((y * pitch_words + x) as usize)
                        .write_volatile(pixel);
                }
            }
        }
    }
}

/// Scroll the display up by one character row and clear the bottom row.
fn scroll_up() {
    // Safety: called from write_byte which checks FB is Some.
    unsafe {
        let fb = match FB {
            Some(ref mut f) => f,
            None => return,
        };

        let scroll_bytes = (fb.height - FONT_H) * fb.pitch;
        let src = (fb.base as *mut u8).add(FONT_H as usize * fb.pitch as usize);
        let dst = fb.base as *mut u8;

        // Move all rows up by FONT_H pixels.
        // src > dst so a forward copy is safe (no overlap per row).
        core::ptr::copy(src, dst, scroll_bytes as usize);

        // Clear the last row.
        let last_row_offset = (fb.height - FONT_H) * (fb.pitch / 4);
        let last_row_words  = FONT_H * (fb.pitch / 4);
        for i in 0..last_row_words {
            fb.base.add((last_row_offset + i) as usize).write_volatile(BG);
        }
    }
}

/// Write a single byte to the text console, handling control characters.
fn write_byte(c: u8) {
    // Safety: single-threaded; no preemption at this stage.
    unsafe {
        let fb = match FB {
            Some(ref mut f) => f,
            None => return,
        };

        match c {
            b'\n' => {
                fb.col = 0;
                fb.row += 1;
            }
            b'\r' => {
                fb.col = 0;
            }
            0x08 | 0x7F => {
                // Backspace
                if fb.col > 0 {
                    fb.col -= 1;
                    draw_char(b' ', fb.col * FONT_W, fb.row * FONT_H);
                }
            }
            _ => {
                let px = fb.col * FONT_W;
                let py = fb.row * FONT_H;
                draw_char(c, px, py);
                fb.col += 1;
                if fb.col >= fb.cols {
                    fb.col = 0;
                    fb.row += 1;
                }
            }
        }

        // Scroll if we've gone past the last row
        if fb.row >= fb.rows {
            scroll_up();
            fb.row = fb.rows - 1;
        }
    }
}

// ── fmt::Write integration ────────────────────────────────────────────────────

/// Zero-sized type that routes `write!` output to the framebuffer console.
/// If the framebuffer is not initialised, writes are silently discarded.
pub struct FbWriter;

impl fmt::Write for FbWriter {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for b in s.bytes() {
            write_byte(b);
        }
        Ok(())
    }
}
