// framebuffer.rs — Pi 4 HDMI framebuffer driver + text console
//
// Uses the VideoCore GPU mailbox property interface to allocate a
// 32 bpp linear framebuffer. Provides a scrolling text console using
// an embedded 8×8 bitmap font. Implements core::fmt::Write so it can
// be used alongside the UART from the kprint! macro.
//
// Hardware invariants relied upon:
//   - MBOX_BUF is 16-byte aligned (required by the mailbox protocol).
//   - On Pi 4, the address returned by the GPU is a direct physical
//     address; no bus-address masking needed.
//   - Caches are disabled (boot.rs never enables them), so pixel writes
//     are immediately visible to the GPU display engine.
//
// Pixel colour format: 0x00_RR_GG_BB stored as a little-endian u32.
// The GPU uses BGR byte order in memory, so:
//   byte at addr+0 = Blue, addr+1 = Green, addr+2 = Red, addr+3 = 0
// Writing 0x00RRGGBB (standard web colour) gives correct colours. If
// red and blue appear swapped on your monitor, swap the R and B
// constants below.

